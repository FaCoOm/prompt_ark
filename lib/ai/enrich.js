// lib/ai/enrich.js — Async AI enrichment (title/category/tags)
import { PromptStorage } from '../storage.js';
import { normalizeTags } from '../text-analysis.js';
import { applyClassificationResult, isConfirmedCategoryType } from '../taxonomy.js';

/**
 * Async AI Enrichment — runs after save, non-blocking
 * @param {string} promptId
 * @param {string} content
 * @param {Function} extractTitleAndCategoryFn - Injected to avoid circular dep
 * @param {Function} getPromptsFn
 * @param {Function} buildContextMenusFn
 */
export async function asyncEnrichPrompt(promptId, content, extractTitleAndCategoryFn, getPromptsFn, buildContextMenusFn, locale = 'zh_CN') {
    try {
        console.log('[asyncEnrich] Starting for prompt:', promptId);
        const result = await extractTitleAndCategoryFn(content);
        console.log('[asyncEnrich] Result:', JSON.stringify(result));
        if (!result || (!result.title && !result.recommended_category_key && !result.tags?.length && !result.output_modality)) {
            console.log('[asyncEnrich] No useful metadata, skipping');
            return;
        }

        const prompts = await getPromptsFn();
        const prompt = prompts.find(p => p.id === promptId);
        if (!prompt) return;

        let changed = false;
        // Only overwrite title if it was auto-generated (not user-typed)
        if (prompt.titleAutoGenerated && result.title) {
            prompt.title = result.title;
            prompt.titleAutoGenerated = false; // AI title is now the real title
            changed = true;
        }
        if (result.output_modality && prompt.output_modality !== result.output_modality) {
            prompt.output_modality = result.output_modality;
            changed = true;
        }
        if (result.tags?.length && normalizeTags(prompt.tags).length === 0) {
            prompt.tags = normalizeTags(result.tags);
            changed = true;
        }
        if (Number.isFinite(Number(result.confidence))) {
            const nextConfidence = Number(result.confidence);
            if (prompt.classification_confidence !== nextConfidence) {
                prompt.classification_confidence = nextConfidence;
                changed = true;
            }
        }

        if (result.recommended_category_key && !isConfirmedCategoryType(prompt.category_type)) {
            const classifiedPrompt = await applyClassificationResult(prompt, result, { locale });
            if (
                classifiedPrompt.category_type !== prompt.category_type ||
                classifiedPrompt.category_key !== prompt.category_key ||
                classifiedPrompt.category !== prompt.category
            ) {
                prompt.category_type = classifiedPrompt.category_type;
                prompt.category_key = classifiedPrompt.category_key;
                prompt.category = classifiedPrompt.category;
                changed = true;
            }
        } else if (result.recommended_category_key) {
            const classifiedPrompt = await applyClassificationResult(prompt, result, { locale });
            if (classifiedPrompt.category !== prompt.category) {
                prompt.category = classifiedPrompt.category;
                changed = true;
            }
        }

        if (changed) {
            await PromptStorage.update(prompt);
            await buildContextMenusFn();
            // Notify popup with the updated prompt data for fast targeted refresh
            try { chrome.runtime.sendMessage({ type: 'PROMPTS_UPDATED', prompt }); } catch { /* popup may be closed */ }
        }
    } catch (e) {
        console.error('[asyncEnrich] Failed:', e);
    }
}
